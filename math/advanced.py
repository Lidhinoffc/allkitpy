import math


def square_root(x):
    return math.sqrt(x)


def cube_root(x):
    return x ** (1 / 3)


def factorial(n):
    return math.factorial(n)


def gcd(a, b):
    return math.gcd(a, b)


def lcm(a, b):
    return math.lcm(a, b)


def absolute(x):
    return abs(x)


def round_number(x, digits=0):
    return round(x, digits)